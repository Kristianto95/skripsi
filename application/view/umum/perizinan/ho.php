    <h3 class="coloumn-title">Pengantar Izin Gangguan</h3>
    <p class="pindex"> Izin Gangguan (HO) adalah izin kegiatan usaha kepada orang pribadi / badan dilokasi tertentu yang berpotensi menimbulkan bahaya kerugian dan gangguan, ketentraman dan ketertiban umum tidak termasuk kegiatan/tempat usaha yang lokasinya telah ditunjuk oleh Pemerintah Pusat atau Daerah.</p>
    <p class="pindex">Saat ini Surat Izin Gangguan di keluarkan oleh Dinas Perizinan Domisili Usaha di daerah tingkat dua atau setingkat Kabupaten dan Kotamadya. Hal ini sesuai dengan diberlakukannya undang-undang otonomi daerah, jadi di tiap - tiap daerah dapat mempunyai aturan yang berbeda dalam mengeluarkan Surat Izin Gangguan. Biasanya untuk mendapatkan Surat Izin Gangguan ini, perusahaan tidak mencemari lingkungan dan atau tidak ada dampak negatif terhadap lingkungan dari usaha yang dilakukan.</p>
