      <h3 class="coloumn-title">Pengantar Surat Izin Usaha Perdagangan</h3>
      <p class="pindex"> Surat Izin Usaha Perdagangan, yang selanjutnya disebut SIUP, adalah Surat Izin untuk dapat melaksanakan kegiatan usaha perdagangan. Setiap perusahaan, koperasi, persekutuan maupun perusahaan perseorangan, yang melakukan kegiatan usaha perdagangan wajib memperoleh SIUP yang diterbitkan berdasarkan domisili perusahaan dan berlaku di seluruh wilayah Republik Indonesia.</p>
      <p class="pindex"> Surat Izin Usaha Perdagangan ini berfungsi sebagai alat atau bukti pengesahan dari usaha perdagangan yang Anda lakukan.
      Surat Izin Usaha Perdagangan di keluarkan oleh pemerintah daerah dan dibutuhkan oleh pelaku usaha perseorangan maupun pelaku usaha yang telah berbadan hukum. Surat Izin Usaha Perdagangan tidak hanya di butuhkan oleh usaha berskala besar saja melainkan juga usaha kecil dan menengah agar usaha yang dilakukan mendapatkan pengakuan dan pengesahan dari pihak pemerintah. Hal ini untuk menghindari terjadi masalah yang dapat mengganggu perkembangan usaha di kemudian hari.</p>
      <p class="pindex">SIUP dikeluarkan berdasarkan domisili pemilik atau penanggungjawabperusahaan. SIUP perusahaan kecil dan menengah diterbitkan dan ditandatangani oleh Kepala Kantor Perindustrian dan Perdagangan Tingkat II atas nama menteri. Sedangkan SIUP perusahaan besar diterbitkan dan ditandatangani oleh Kepala Kantor Perindustrian dan Perdagangan Daerah Tingkat I atas nama menteri.</p>
