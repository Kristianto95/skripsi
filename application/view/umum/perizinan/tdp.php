<div class="container-fluid kotak2">
  <h3>Pengantar Tanda Daftar Perusahaan</h3>
  <p class="pindex">Tanda Daftar Perusahaan (TDP) adalah daftar catatan resmi yang diadakan menurut atau berdasarkan ketentuan undang-undang atau peraturan-peraturan pelaksanaannya, dan memuat hal-hal yang wajib didaftarkan oleh setiap perusahaan serta disahkan oleh pejabat yang berwenang.</p>
  <p class="pindex"> Setiap perusahaan yang berbentuk Perseroan Terbatas (PT), Koperasi, Persekutuan Komanditer (CV), Firma (Fa), Perorangan, dan Bentuk Usaha Lainnya (BUL), termasuk Perusahaan Asing dengan status Kantor Pusat, Kantor Tunggal, Kantor Cabang, Kantor Pembantu, Anak Perusahaan, Agen Perusahaan, dan Perwakilan Perusahaan yang berkedudukan dan menjalankan usahanya di wilayah Negara Kesatuan Republik Indonesia wajib didaftarkan dalam daftar perusahaan.</p>
<p class="pindex">Tanda Daftar Perusahaan berlaku selama Perusahaan tersebut masih beroperasi dan wajib didaftarkan ulang setiap 5 (lima) tahun.</p>
