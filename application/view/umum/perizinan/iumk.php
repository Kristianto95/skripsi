      <h3 class="coloumn-title">Pengantar Izin Usaha Mikro dan Kecil</h3>
      <p class="pindex"> Izin usaha mikro dan kecil yang selanjutnya disingkat dengan IUMK adalah tanda legalitas kepada seseorang atau pelaku usaha/kegiatan tertentu dalam bentuk izin usaha mikro dan kecil dalam bentuk satu lembar. Manfaatnya setelah mendapatkan Izin Usaha Mikro Kecil atau IUMK, para pelaku usaha bisa mendapatkan kepastian berusaha dan akses pembiayaan perbankan/non perbankan. Selain itu para pelaku usaha mikro dan kecil itu juga akan dibekali pendampingan dan pemberdayaan. Jadi intinya kebijakan penerbitan IUMK maka ke depan para pelaku UMK telah mendapat kepastian dan perlindungan dalam berusaha di lokasi yang ditetapkan. Disamping itu juga dengan IUMK ini para pelaku UMK dapat lebih mudah mengakses ke sumber-sumber daya produktif, seperti akses teknologi, akses pasar, dan akses pelatihan SDM.</p>
